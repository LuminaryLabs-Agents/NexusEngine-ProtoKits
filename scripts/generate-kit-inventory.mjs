import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, posix } from "node:path";

const ROOT = process.cwd();
const PROTOKITS = join(ROOT, "protokits");
const OUT = join(ROOT, "docs", "PROTOKIT-INVENTORY.md");

function readJson(path) {
  try { return JSON.parse(readFileSync(path, "utf8")); }
  catch { return null; }
}

function has(path) { return existsSync(path) ? "yes" : "no"; }

const entries = [];
if (existsSync(PROTOKITS)) {
  for (const entry of readdirSync(PROTOKITS, { withFileTypes: true }).filter((item) => item.isDirectory()).sort((a, b) => a.name.localeCompare(b.name))) {
    const dir = join(PROTOKITS, entry.name);
    const manifest = readJson(join(dir, "kit.manifest.json"));
    entries.push({
      path: posix.join("protokits", entry.name),
      id: manifest?.id ?? entry.name,
      domain: manifest?.domain ?? "undocumented",
      type: manifest?.type ?? "undocumented",
      status: manifest?.status ?? "undocumented",
      factory: manifest?.factory ?? "see index.js",
      readme: has(join(dir, "README.md")),
      manifest: manifest ? "yes" : "no",
      provides: (manifest?.provides ?? []).join(", ")
    });
  }
}

const lines = [
  "# ProtoKit Inventory",
  "",
  "Generated by `scripts/generate-kit-inventory.mjs`.",
  "",
  "| Path | ID | Domain | Type | Status | Factory | README | Manifest | Provides |",
  "|---|---|---|---|---|---|---|---|---|",
  ...entries.map((entry) => `| \`${entry.path}\` | \`${entry.id}\` | ${entry.domain} | ${entry.type} | ${entry.status} | \`${entry.factory}\` | ${entry.readme} | ${entry.manifest} | ${entry.provides} |`),
  ""
];

mkdirSync(join(ROOT, "docs"), { recursive: true });
writeFileSync(OUT, lines.join("\n"));
console.log(`Wrote ${entries.length} ProtoKit inventory entries to ${OUT}.`);
